characters = ['Tom', 'Jerry', 'Butch', 'Tuffy', 'Pecos']

# Print the full list
print(characters)

# Print the first element
print(characters[0])  # Tom

# Print the last element
print(characters[-1])  # Pecos

# Print a sublist (from index 2 up to but not including index 4)
print(characters[2:4])  # ['Butch', 'Tuffy']